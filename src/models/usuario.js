class Usuario {
    constructor(idUsuario, apellido, nombre, nombreUsuario, contraseña, correo, telefono, tipo) {
        this.idUsuario = idUsuario;
        this.apellido = apellido,
        this.nombre = nombre;
        this.nombreUsuario = nombreUsuario;
        this.contraseña = contraseña;
        this.correo = correo;
        this.telefono = telefono;
        this.tipo = tipo;
    }
}